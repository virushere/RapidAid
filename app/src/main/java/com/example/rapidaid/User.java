package com.example.rapidaid;

public class User {
    String name, email, phone, vehicle, city, work;

    public User() {
    }

    public User(String name, String email, String phone, String vehicle, String city, String work) {
        this.name = name;
        this.email = email;
        this.phone = phone;
        this.vehicle = vehicle;
        this.city = city;
        this.work = work;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getVehicle() {
        return vehicle;
    }

    public void setVehicle(String vehicle) {
        this.vehicle = vehicle;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getWork() {
        return work;
    }

    public void setWork(String work) {
        this.work = work;
    }
}
